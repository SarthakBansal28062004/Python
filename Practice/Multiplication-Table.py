def multiplication_table():
    # Outer loop for the multiplicand (1 to 10)
    for i in range(1, 11):
        # Inner loop for the multiplier (1 to 10)
        for j in range(1, 11):
            # Calculate and print the product
            product = i * j
            print(f"{i} * {j} = {product}")
        # Print a newline after each row of the table
        print()


# Call the function to generate the multiplication table
multiplication_table()

